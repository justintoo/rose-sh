TIME_DEFAULT_CONFIGURE_OPTIONS=()
: ${TIME_CONFIGURE_OPTIONS:=${TIME_DEFAULT_CONFIGURE_OPTIONS[@]}}

#-------------------------------------------------------------------------------
download_time()
#-------------------------------------------------------------------------------
{
  info "Downloading source code"

  set -x
      clone_repository "${application}" "${application}-src" || exit 1
      cd "${application}-src/" || exit 1
  set +x
}

#-------------------------------------------------------------------------------
install_deps_time()
#-------------------------------------------------------------------------------
{
  install_deps ${TIME_DEPENDENCIES} || exit 1
}

#-------------------------------------------------------------------------------
patch_time()
#-------------------------------------------------------------------------------
{
  info "Patching"
}

#-------------------------------------------------------------------------------
configure_time__rose()
#-------------------------------------------------------------------------------
{
  info "Configuring application for ROSE compiler='${ROSE_CC}'"

  set -x
      CC="${ROSE_CC}" \
        ./configure \
          "${TIME_CONFIGURE_OPTIONS[@]}" \
          || exit 1
  set +x
}

#-------------------------------------------------------------------------------
configure_time__gcc()
#-------------------------------------------------------------------------------
{
  info "Configuring application for default compiler='${CC}'"

  set -x
      ./configure \
          "${TIME_CONFIGURE_OPTIONS[@]}" \
          || exit 1
  set +x
}

