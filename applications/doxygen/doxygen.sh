: ${DOXYGEN_DEPENDENCIES:=}

#-------------------------------------------------------------------------------
download_doxygen()
#-------------------------------------------------------------------------------
{
  info "Downloading source code"

  set -x
      clone_repository "${application}" "${application}-src" || exit 1
      cd "${application}-src/" || exit 1
  set +x
}

#-------------------------------------------------------------------------------
install_deps_doxygen()
#-------------------------------------------------------------------------------
{
  install_deps ${DOXYGEN_DEPENDENCIES} || fail "Could not install dependencies"
}

#-------------------------------------------------------------------------------
patch_doxygen()
#-------------------------------------------------------------------------------
{
  info "Patching application"
}

#-------------------------------------------------------------------------------
configure_doxygen__rose()
#-------------------------------------------------------------------------------
{
  info "Configuring application for ROSE compiler='${ROSE_CC}'"

  #-----------------------------------------------------------------------------
  set -x
  #-----------------------------------------------------------------------------
      export CC_ROSE_TRANSLATOR="${ROSE_CC}"
      export CXX_ROSE_TRANSLATOR="${ROSE_CXX}"

      ./configure --prefix "$(pwd)/install_tree" || exit 1
  #-----------------------------------------------------------------------------
  set +x
  #-----------------------------------------------------------------------------
}

#-------------------------------------------------------------------------------
configure_doxygen__gcc()
#-------------------------------------------------------------------------------
{
  info "Configuring application for default compiler='${CC}'"

  #-----------------------------------------------------------------------------
  set -x
  #-----------------------------------------------------------------------------
      CC="${CC}" \
          ./configure --prefix "$(pwd)/install_tree" || exit 1

      export CC="${CC}"
  #-----------------------------------------------------------------------------
  set +x
  #-----------------------------------------------------------------------------
}

#-------------------------------------------------------------------------------
compile_doxygen()
#-------------------------------------------------------------------------------
{
  info "Compiling application"

  set -x
      make -j${parallelism} -C qtools/  || exit 1
      make -j${parallelism} -C libmd5/ || exit 1
      # Cannot be run in parallel (version.cpp?)
      echo "char versionString[]=\"1.8.3.1-20130209\";" > src/version.cpp
      make -j${parallelism} -C src/ || exit 1
  set +x
}
