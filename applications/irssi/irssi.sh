#-------------------------------------------------------------------------------
download_irssi()
#-------------------------------------------------------------------------------
{
  info "Downloading source code"

  set -x
      clone_repository "${application}" "${application}-src" || exit 1
      cd "${application}-src/" || exit 1
  set +x
}

#-------------------------------------------------------------------------------
install_deps_irssi()
#-------------------------------------------------------------------------------
{
  install_deps ${IRSSI_DEPENDENCIES} || fail "Could not install dependencies"
}

#-------------------------------------------------------------------------------
patch_irssi()
#-------------------------------------------------------------------------------
{
  info "Patching not required"
}

#-------------------------------------------------------------------------------
configure_irssi__rose()
#-------------------------------------------------------------------------------
{
  info "Configuring application for ROSE compiler='${ROSE_CC}'"

  #-----------------------------------------------------------------------------
  set -x
  #-----------------------------------------------------------------------------
      ./configure \
          || exit 1

  #-----------------------------------------------------------------------------
  set +x
  #-----------------------------------------------------------------------------
}

#-------------------------------------------------------------------------------
configure_irssi__gcc()
#-------------------------------------------------------------------------------
{
  info "Configuring application for default compiler='${CC}'"

  #-----------------------------------------------------------------------------
  set -x
  #-----------------------------------------------------------------------------
      ./configure \
          --prefix="$(pwd)/install_tree" \
          --disable-yasm \
          \
              --enable-shared \
              --disable-doc \
              --enable-pthreads \
              --disable-w32threads \
              --disable-os2threads \
              --enable-zlib \
              --enable-openssl \
              --disable-asm \
              --enable-pic \
              --extra-cflags="$CFLAGS" \
              --extra-ldflags="$LDFLAGS" \
          \
          || exit 1

      export CC="${CC}"
  #-----------------------------------------------------------------------------
  set +x
  #-----------------------------------------------------------------------------
}

#-------------------------------------------------------------------------------
compile_irssi()
#-------------------------------------------------------------------------------
{
  info "Compiling application"

  # TOO1 (2/6/2014) We'll just always enable VERBOSE output
  set -x
      make CC="${ROSE_CC} -rose:C89_only" -j${parallelism} || exit 1
  set +x
}
